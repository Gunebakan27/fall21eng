public class ExceptionNotes {
/*
 * 1)throws, throw, try, catch, finally, compile time exception(checked), runtime exception(unchecked)
 * 2)Difference between "throw" and "throws"
 * 	i)"throw" is used in method body. But "throws" is used in method name line.
 * 	ii)After "throw" keyword, we create an object by using Exception classes. But
 * 	after "throws" keyword we type just the name of Exception class.
 * 	iii)After "throws" keyword you can use multiple Exception class names by separating by come but after "throw" keyword 
 * 	you can create just a single object. 
 * 		Note;After "throws" keyword, parent and child Exception classes cannot be used together, Java removes
 * 			child and replace parent automatically.
 * 	iv)"throws" can be used once at most in a method but "throw" can be used many times in a method body.
 * 
 */
	
}
